int	ft_sqrt(int nb)
{
	int i;

	if (nb > 0)
	{
		i = 1;
		while (i * i < nb && i < 46341)
		{
			i = i + 1;
		}
		if (i * i == nb)
		{
			return (i);
		}
	}
	return (0);
}
