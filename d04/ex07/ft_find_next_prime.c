int	ft_find_next_prime(int nb)
{
	int			i;
	long int	j;

	if (nb <= 2)
	{
		return (2);
	}
	i = nb + (nb + 1) % 2;
	j = 3;
	while (j * j <= i)
	{
		if (i % j == 0)
		{
			i = i + 2;
			j = 1;
		}
		j = j + 2;
	}
	return (i);
}
