int	ft_is_prime(int nb)
{
	long int i;

	if ((nb % 2 == 0 && nb != 2) || nb <= 1)
	{
		return (0);
	}
	else if (nb == 2)
	{
		return (1);
	}
	else
	{
		i = 3;
		while (i * i <= nb)
		{
			if (nb % i == 0)
			{
				return (0);
			}
			i = i + 2;
		}
	}
	return (1);
}
