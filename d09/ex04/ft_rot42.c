char	*ft_rot42(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] <= 'Z' && str[i] >= 'A')
			str[i] = 'A' + (((str[i] - 'A') + 16) % 26);
		else if (str[i] <= 'z' && str[i] >= 'a')
			str[i] = 'a' + (((str[i] - 'a') + 16) % 26);
		++i;
	}
	return (str);
}
