char	*ft_strrev(char *str)
{
	char	tmp;
	int		n;
	int		i;

	n = 0;
	i = 0;
	while (str[n] != '\0')
	{
		n = n + 1;
	}
	while (i < n / 2)
	{
		tmp = str[i];
		str[i] = str[n - 1 - i];
		str[n - 1 - i] = tmp;
		i = i + 1;
	}
	return (str);
}
