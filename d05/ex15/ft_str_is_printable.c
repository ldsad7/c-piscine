int	ft_str_is_printable(char *str)
{
	int i;
	int fl;

	fl = 0;
	i = 0;
	while (str[i] != '\0' && fl == 0)
	{
		if (str[i] < ' ' || str[i] > '~')
		{
			fl = 1;
			break ;
		}
		++i;
	}
	if (fl == 0)
		return (1);
	else
		return (0);
}
