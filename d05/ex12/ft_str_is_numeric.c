int	ft_str_is_numeric(char *str)
{
	int i;
	int fl;

	fl = 0;
	i = 0;
	while (str[i] != '\0' && fl == 0)
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			fl = 1;
			break ;
		}
		++i;
	}
	if (fl == 0)
		return (1);
	else
		return (0);
}
